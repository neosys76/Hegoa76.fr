Au commencement était ... ?</div>
<p class="description">
Ma foi, l'ami, si tu attends une réponse à cette question, je serais bien en peine de te dire ce qu'il y avait au commencement, ni même s'il y eut un commencement... 
Comment savoir ? Le monde a-t-il été créé par un Dieu unique ou par une multitude de divinités ? Est-il issu d'un oeuf ou est-il lui-même un oeuf ? 
L'univers a-t-il une origine et une finalité ou les choses ne sont-elles que l'éternel recommencement du même ? Les esprits les plus perspicaces débattent sans fin de ces insondables questions... 
Tout ce que je peux te dire, c'est qu'il existe au moins deux mondes, peut-être plus... Le premier, celui qui nous abrite, nous les D'juns, nous l'appelons "Daogad". 
C'est un monde de pure spiritualité, les humains l'appellent le monde de l'invisible ou l'au-delà... 
Le deuxième monde, celui que tu découvriras bientôt, se nomme "Hégoa". C'est le domaine où vivent les humains et tous les peuples de la création. Enfin, certains mystiques prétendent qu'il existe encore d'autres mondes, d'autres niveaux d'existence... 
Je ne sais si cette légende est fondée et si ces terres mythiques existent mais peut-être un jour aurons-nous la chance de les explorer... 
