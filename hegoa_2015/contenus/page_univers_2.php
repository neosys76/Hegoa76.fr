<div class="titre">Au commencement &eacute;tait ... ?</div>

<p class="description">
Ma foi, l'ami, si tu attends une r&eacute;ponse &agrave cette question, je serais bien en peine de te dire ce qu'il y avait au commencement, ni m&ecirc;me s'il y eut un commencement...

Comment savoir ? Le monde a-t-il &eacute;t&eacute; cr&eacute;&eacute; par un Dieu unique ou par une multitude de divinit&eacute;s ? Est-il issue d'un oeuf ou est-il lui-m&ecirc;me un oeuf ? L'univers a-t-il une origine et une finalit&eacute; ou les choses ne sont-elles que l'&eacute;ternel recommencement du m&ecirc;me ? Les esprits les plus perspicaces d&eacute;battent sans fin de ces insondables questions...

Tout ce que je peux te dire, c'est qu'il existe au moins deux mondes, peut-&ecirc;tre plus... Le premier, celui qui nous abrite, nous les D'juns, nous l'appelons "Daogad". C'est un monde de pure spiritualit&eacute;, les humains l'appellent le monde de l'invisible ou l'au-del&agrave...

Le deuxi&egrave;me monde, celui que tu d&eacute;couvriras bient&ocirc;t, se nomme "H&eacute;goa". C'est le domaine o&ugrave; vivent les humains et tous les peuples de la cr&eacute;ation.

Enfin, certains mystiques pr&eacute;tendent qu'il existe encore d'autres mondes, d'autres niveaux d'existence... Je ne sais si cette l&eacute;gende est fond&eacute;e et si ces terres mythiques existent mais peut-&ecirc;tre un jour aurons-nous la chance de les explorer...
